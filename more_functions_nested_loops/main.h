#ifndef MAIN_H
#define MAIN_H
/**
 * _isupper - checks for uppeercase character
 * @c: character
 * Return: 1 if c is uppercase; 0 otherwise
 */
int _isupper(int c);
/**
 * _putchar - writes the character c to stdout
 * @c: The character to print
 *
 * Return: On success 1.
 * On error, -1 is returned, and errno is set appropriately.
 */
int _putchar(char c);
#endif
